'''
this is a destructive sort, assuming its not the most efficient one, but 
should work for coding interviews

common ones are bubble sort, insertion sort, selection sort, quick sort, 
merge sort, heap sort, counting sort

bubble sort -> compare first two element. If they out of order, swap
do this n-1, then n-2 (except for the last element), ... 
so n(n-1)/2 so O(n^2) time complex, but O(1) space complexity 

Selection sort -> search smallest array, swap with first, repeat this 
very intuitive 

Insertion sort -> it's like playing big 2. U take out a card and place it 
at the right position. better than bubble, selection sort 
Go from low to high and insert them at the right position. start @ index 1

quick sort -> we have a pivot element (rightmost or something like that)
whatever is less goes left, whatever greater goes right
keep repeating this nlogn or n^2 complexity 

Merge sort -> most popular sorting algorithm i guess
nlogn run time, divide and conquer 

'''
  
# Merges two subarrays of arr[]. 
# First subarray is arr[l..m] 
# Second subarray is arr[m+1..r] 
def merge(arr, l, m, r): 
    n1 = m - l + 1
    n2 = r- m 
  
    # create temp arrays 
    L = [0] * (n1) 
    R = [0] * (n2) 
  
    # Copy data to temp arrays L[] and R[] 
    for i in range(0 , n1): 
        L[i] = arr[l + i] 
  
    for j in range(0 , n2): 
        R[j] = arr[m + 1 + j] 
  
    # Merge the temp arrays back into arr[l..r] 
    i = 0     # Initial index of first subarray 
    j = 0     # Initial index of second subarray 
    k = l     # Initial index of merged subarray 
  
    while i < n1 and j < n2 : 
        if L[i] <= R[j]: 
            arr[k] = L[i] 
            i += 1
        else: 
            arr[k] = R[j] 
            j += 1
        k += 1
  
    # Copy the remaining elements of L[], if there 
    # are any 
    while i < n1: 
        arr[k] = L[i] 
        i += 1
        k += 1
  
    # Copy the remaining elements of R[], if there 
    # are any 
    while j < n2: 
        arr[k] = R[j] 
        j += 1
        k += 1
  
# l is for left index and r is right index of the 
# sub-array of arr to be sorted 
def mergeSort(arr,l,r): 
    if l < r: 
  
        # Same as (l+r)/2, but avoids overflow for 
        # large l and h 
        m = (l+(r-1))/2
  
        # Sort first and second halves 
        mergeSort(arr, l, m) 
        mergeSort(arr, m+1, r) 
        merge(arr, l, m, r) 
  
